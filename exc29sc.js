//  Crie um programa que
//  solicite ao usuário que digite um número de 1 a 7, e então imprima o dia da 
//  semana correspondente (1 = Segunda-feira, 2 = Terça-feira, etc).
let numero = parseInt(prompt('digite um número de 1 a 7:'));
switch (numero){
    case 1:
        alert('segunda-feira')
        break
    case 2:
        alert('terça-feira')
        break
    case 3:
        alert('quarta-feira')
        break
    case 4:
        alert('quinta-feira')
        break
    case 5:
        alert('sexta-feira')
        break
    case 6:
        alert('sabado')
        break
    case 7:
        alert('domingo')
    


}